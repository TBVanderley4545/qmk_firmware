#include QMK_KEYBOARD_H

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case DK_PREV:
            if(record->event.pressed) {
                SEND_STRING(SS_PREV_DESKTOP);
            }
            break;

        case DK_NEXT:
            if(record->event.pressed) {
                SEND_STRING(SS_NEXT_DESKTOP);
            }
            break;

        case DK_REVEAL:
            if(record->event.pressed) {
                SEND_STRING(SS_REVEAL_DESKTOPS);   
            }
            break;
    }
    return true;
};

